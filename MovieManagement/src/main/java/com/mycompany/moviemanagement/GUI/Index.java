    private void generateMoviePanels(List<Movie> movies) {   
        this.jPanelContainer.removeAll();
        
        movies.forEach(m -> {
            MoviePanel moviePanel = new MoviePanel(m, userDao);
            moviePanel.setPreferredSize(new Dimension(200,300));
            this.jPanelContainer.add(moviePanel);
        });
        this.validate();
    }
    
