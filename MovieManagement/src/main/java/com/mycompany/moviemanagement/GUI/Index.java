    private void jBtnInsertMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnInsertMovieActionPerformed
        // TODO add your handling code here:
        InsertFrame frame = new InsertFrame();
        frame.setVisible(true);
        
        if(!frame.isDisplayable()) {
            List<Movie> newMovies = movieDao.getAllMovies();
            generateMoviePanels(newMovies);
        }
    }//GEN-LAST:event_jBtnInsertMovieActionPerformed

    private void jTFSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFSearchKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            String text = jTFSearch.getText();
            if(text.isBlank()) {
                List<Movie> movies = movieDao.getAllMovies();
                generateMoviePanels(movies);
            }
            List<Movie> movies = movieDao.search(text);
            generateMoviePanels(movies);
            
        }
    }//GEN-LAST:event_jTFSearchKeyPressed

