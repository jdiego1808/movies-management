    private void jBtnInsertFromFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnInsertFromFileActionPerformed
        
        CardLayout layout = (CardLayout) jPanel2.getLayout();
            
        JPopupMenu updateOption = new JPopupMenu("Update Option");
        JMenuItem credits = new JMenuItem("Stars");
        updateOption.add(credits);
        credits.addActionListener((ActionEvent e) -> {
            List<Credit> c = readFileCredits();
            c.forEach(i -> {
                creditsTableModel.addRow(new Object[] {i.getName(), i.getGender(), i.getType(), i.getCharacter(), i.getPosition()});
            });
            jTableCredits.validate();
        });
        JMenuItem locations = new JMenuItem("Locations");
        updateOption.add(locations);
        locations.addActionListener((ActionEvent e) -> {
            List<String> locs = readFileLocations();
            locs.forEach(i -> {
                locationsListModel.addElement(i);
            });
            jListLocations.validate();
        }); 
        JMenuItem relDates = new JMenuItem("Release Dates");
        updateOption.add(relDates);
        relDates.addActionListener((ActionEvent e) -> {
            List<ReleaseDate> rd = readFileReleaseDates();
            rd.forEach(i -> {
                releaseDatesTableModel.addRow(new Object[] {i.getCountry(), i.getDate()});
            });
            jTableReleaseDates.validate();
        }); 
        
        updateOption.show(jPanel1, jBtnInsertFromFile.getLocation().x, jBtnInsertFromFile.getLocation().y);
        
        
    }//GEN-LAST:event_jBtnInsertFromFileActionPerformed

    private void jBtnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnFinishActionPerformed
        String title = jTFTitle.getText();
        if(title.isBlank()) {
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        List<String> genres = new ArrayList<>();
        String[] genresTmp = jTAreaGenres.getText().split(",");
        for(String s: genresTmp) genres.add(s.trim());
        
        String runningTimes = jTFRunningtime.getText();
        if(runningTimes.isBlank()) {
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        
        List<String> keywords = new ArrayList<>();
        String[] keywordsTmp = jTextKeywords.getText().split(",");
        if(jTextKeywords.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        for(String s: keywordsTmp) keywords.add(s.trim());        
        
        List<String> languages = new ArrayList<>();
        if(languagesListModel.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        for(int i=0;i<languagesListModel.getSize(); i++) {
            languages.add(languagesListModel.get(i).toString());
        }
        
        List<String> locations = new ArrayList<>();
        if(locationsListModel.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        for(int i=0;i<locationsListModel.getSize(); i++) {
            locations.add(locationsListModel.get(i).toString());
        }
        
        List<ReleaseDate> relDates = new ArrayList<>();
        if(releaseDatesTableModel.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        int year = Integer.MAX_VALUE;
        for(int i=0; i<releaseDatesTableModel.getRowCount();i++) {
            String date = releaseDatesTableModel.getValueAt(i, 0).toString();
            String country = releaseDatesTableModel.getValueAt(i, 1).toString();
            Pattern pattern = Pattern.compile("(\\d{4})");
            Matcher matcher = pattern.matcher(date);
            matcher.find();
            int minYear = Integer.parseInt(matcher.group(1));
            if(minYear<year) year=minYear;
            relDates.add(new ReleaseDate(country, date));
        }
        
        List<Credit> credits = new ArrayList<>();
        if(creditsTableModel.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Invalid data", "Error", ERROR_MESSAGE);
            return;
        }
        for(int i=0; i<creditsTableModel.getRowCount();i++) {
            credits.add(new Credit(
                creditsTableModel.getValueAt(i, 0).toString(), 
                creditsTableModel.getValueAt(i, 1).toString(),
                creditsTableModel.getValueAt(i, 2).toString(),
                creditsTableModel.getValueAt(i, 3).toString(),
                Integer.parseInt(creditsTableModel.getValueAt(i, 4).toString())
            ));
        }
        
        Movie movie = new Movie(title, year, genres, keywords, languages, locations, relDates, runningTimes, credits);
        movieDao.createMovie(movie);
        JOptionPane.showMessageDialog(this, "Insert successfully");
        this.dispose();
    }//GEN-LAST:event_jBtnFinishActionPerformed

