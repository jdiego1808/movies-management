    private List<String> readFileLocations() {
        List<String> objList = new ArrayList<>();
        JFileChooser fileChooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int r = fileChooser.showOpenDialog(null);
        if (r == JFileChooser.APPROVE_OPTION) {
            
            try {
                File myObj = fileChooser.getSelectedFile();
                Scanner myReader = new Scanner(myObj);                
                while (myReader.hasNextLine()) {
                    String data = myReader.nextLine().trim();
                    objList.add(data);
                }
                myReader.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(InsertFrame.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
        return objList;
    }
    
    private List<ReleaseDate> readFileReleaseDates() {
        List<ReleaseDate> objList = new ArrayList<>();
        JFileChooser fileChooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int r = fileChooser.showOpenDialog(null);
        if (r == JFileChooser.APPROVE_OPTION) {
            
            try {
                File myObj = fileChooser.getSelectedFile();
                Scanner myReader = new Scanner(myObj);                
                while (myReader.hasNextLine()) {
                    String[] data = myReader.nextLine().split("|");
                    ReleaseDate relDate = new ReleaseDate(data[0].trim(), data[1].trim());                    
                    objList.add(relDate);
                }
                myReader.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(InsertFrame.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
        return objList;
    }
    
